	IMPLICIT REAL*8(A-H)
	IMPLICIT REAL*8(O-Z)
	OPEN(1,STATUS='UNKNOWN',FILE='DATFIL')
	W=2.
	T=0.
	S=0.
	X=0.
	XD=W
	H=.01
	DO WHILE(T<=10.)
		S=S+H
		XDD=-W*W*X
		XD=XD+H*XDD
		X=X+H*XD
		T=T+H
		IF(S>=.09999)THEN
			S=0.
			XTHEORY=SIN(W*T)
			WRITE(9,*)T,X,XTHEORY
			WRITE(1,*)T,X,XTHEORY
		ENDIF
	END DO
	!PAUSE
	CLOSE(1)
	END